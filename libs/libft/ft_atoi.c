int	ft_atoi(const char *str)
{
	int	rest;
	int	i;
	int	s;

	rest = 0;
	i = 0;
	s = 1;
	while ((str[i] >= '\t' && str[i] <= '\r' ) || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			s = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		rest = (rest * 10) + (str[i] - 48);
		i++;
	}
	return (rest * s);
}
